import type { Place, QuickFact } from '@/src/types/place';

const lastUpdated = '2026-03-20';

const sourceUrlFromId = (sourceId: string) => {
  const [kind, id] = sourceId.split(':');
  return `https://www.openstreetmap.org/${kind}/${id}`;
};

type CreatePlaceInput = Omit<
  Place,
  'source' | 'sourceUrl' | 'sourceRefs' | 'gallery' | 'attribution' | 'lastUpdated'
> & {
  gallery?: string[];
};

const facts = (...items: QuickFact[]) => items;

const place = (input: CreatePlaceInput): Place => ({
  ...input,
  source: 'OpenStreetMap',
  sourceUrl: sourceUrlFromId(input.sourceId),
  sourceRefs: [{ label: 'OpenStreetMap', url: sourceUrlFromId(input.sourceId) }],
  attribution: null,
  gallery: input.gallery ?? [],
  lastUpdated,
});

